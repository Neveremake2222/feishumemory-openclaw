"""Track J — Retrieval Quality Benchmark (30 cases).

Tests memory retrieval quality independently from agent task success.
Each case measures Recall@K, Precision@K, NDCG@10, MRR, and latency.

Coverage by memory_type_dimension:
  fact        J-01 ~ J-06  (6 cases)
  decision    J-07 ~ J-12  (6 cases)
  preference  J-13 ~ J-17  (5 cases)
  procedural  J-18 ~ J-21  (4 cases)
  artifact    J-22 ~ J-24  (3 cases)
  social      J-25 ~ J-27  (3 cases)
  event       J-28 ~ J-30  (3 cases)
"""

from __future__ import annotations

from benchmarks.structures import (
    BenchmarkCase,
    Complexity,
    Direction,
    InterferenceSetup,
    RecallSpec,
    ResultAssertion,
    SetupEvent,
    SetupMemory,
    _standard_ground_truth,
    _standard_rubric,
)


def _m(
    memory_type: str,
    title: str,
    summary: str,
    content: dict,
    *,
    importance: float = 0.7,
    confidence: float = 0.85,
    evidence: list[dict] | None = None,
    tags: list[str] | None = None,
    created_hours_ago: float = 0.0,
    scope: str = "project",
    project_id: str = "proj_alpha",
    task_id: str | None = None,
    user_id: str | None = None,
) -> SetupMemory:
    return SetupMemory(
        memory_type=memory_type,
        title=title,
        summary=summary,
        content=content,
        importance=importance,
        confidence=confidence,
        evidence=evidence or [{"source_ref": "agent://track-j"}],
        tags=tags or [],
        created_hours_ago=created_hours_ago,
        scope=scope,
        project_id=project_id,
        task_id=task_id,
        user_id=user_id,
    )


def _recall(
    query: str,
    project_id: str = "proj_alpha",
    user_id: str | None = None,
    task_id: str | None = None,
    intent: str = "general",
    limit: int = 5,
    assertions: list[ResultAssertion] | None = None,
) -> RecallSpec:
    return RecallSpec(
        query=query,
        project_id=project_id,
        user_id=user_id,
        task_id=task_id,
        intent=intent,
        limit=limit,
        assertions=assertions or [],
    )


