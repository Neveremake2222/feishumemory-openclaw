def _memory(
    memory_type: str,
    title: str,
    summary: str,
    content: dict,
    *,
    importance: float = 0.7,
    confidence: float = 0.85,
    evidence: list[dict] | None = None,
    tags: list[str] | None = None,
    created_hours_ago: float = 0.0,
    scope: str = "project",
    project_id: str | None = "proj-alpha",
    task_id: str | None = None,
    user_id: str | None = None,
) -> SetupMemory:
    return SetupMemory(
        memory_type=memory_type,
        title=title,
        summary=summary,
        content=content,
        importance=importance,
        confidence=confidence,
        evidence=evidence or [{"source_ref": "agent://track-i"}],
        tags=tags or [],
        created_hours_ago=created_hours_ago,
        scope=scope,
        project_id=project_id,
        task_id=task_id,
        user_id=user_id,
    )


def _title_ground_truth(
    *,
    expected_titles: list[str] | None = None,
    forbidden_titles: list[str] | None = None,
    expected_count_range: tuple[int, int] | None = None,
    expect_zero_results: bool = False,
    required_content: list[str] | None = None,
) -> dict:
    return {
        "expected_titles": expected_titles or [],
        "forbidden_titles": forbidden_titles or [],
        "expected_count_range": expected_count_range,
        "expect_zero_results": expect_zero_results,
        "required_content": required_content or [],
    }


def _title_rubric(
    *,
    expected_titles: list[str] | None = None,
    forbidden_titles: list[str] | None = None,
    bounded_context: bool = True,
    answer_required: bool = True,
) -> dict:
    criteria = []
    if expected_titles:
        criteria.append({
            "name": "recall_required_memory",
            "weight": 0.35,
            "description": "Selected context includes every expected memory title.",
        })
    if forbidden_titles:
        criteria.append({
            "name": "exclude_wrong_or_stale_memory",
            "weight": 0.25,
            "description": "Selected context excludes stale, cross-scope, or otherwise forbidden memories.",
        })
    if bounded_context:
        criteria.append({
            "name": "bounded_context",
            "weight": 0.15,
            "description": "Recall returns only the expected bounded amount of context.",
        })
    if answer_required:
        criteria.append({
            "name": "answer_uses_memory",
            "weight": 0.25,
            "description": "Answer-level evaluation is faithful, relevant, and improved by memory.",
        })
    if not criteria:
        criteria.append({
            "name": "refusal_correctness",
            "weight": 1.0,
            "description": "No unsupported memory is returned for an unknown request.",
        })
    return {
        "score_type": "weighted_diagnostics",
        "pass_threshold": 1.0,
        "criteria": criteria,
    }


