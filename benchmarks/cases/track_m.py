"""Track M: Project Management Business Value.

These cases keep the assertions strict: the project-memory assistant must recall
the specific decision/status/risk memories that make a PM workflow possible.
"""

from __future__ import annotations

from benchmarks.structures import BenchmarkCase, RecallSpec, ResultAssertion, SetupMemory


PROJECT_ID = "proj_track_m_delivery"


def _memory(
    memory_type: str,
    title: str,
    summary: str,
    content: dict,
    *,
    tags: list[str] | None = None,
    importance: float = 0.8,
    confidence: float = 0.9,
    source_ref: str = "track_m://setup",
) -> SetupMemory:
    payload = {"scope": "project", **content}
    return SetupMemory(
        memory_type=memory_type,
        title=title,
        summary=summary,
        content=payload,
        importance=importance,
        confidence=confidence,
        evidence=[{"source_ref": source_ref}],
        tags=tags or [],
        project_id=PROJECT_ID,
        scope="project",
    )


def _case(
    case_id: str,
    capability: str,
    description: str,
    setup_memories: list[SetupMemory],
    query: str,
    expected_titles: list[str],
    *,
    forbidden_titles: list[str] | None = None,
    expected_count_range: tuple[int, int] | None = None,
    intent: str = "project_summary",
    memory_target: str = "project_business_value",
    difficulty: str = "medium",
    metric_name: str = "business_value",
    metric_value: float = 1.0,
) -> BenchmarkCase:
    return BenchmarkCase(
        case_id=case_id,
        track="M",
        capability=capability,
        description=description,
        direction="B+C",
        complexity_reasoning="medium",
        complexity_tool="low",
        complexity_interaction="low",
        memory_types=sorted({item.memory_type for item in setup_memories}),
        setup_memories=setup_memories,
        recalls=[
            RecallSpec(
                query=query,
                project_id=PROJECT_ID,
                scope="project",
                intent=intent,
                limit=5,
            )
        ],
        assertions=[ResultAssertion("evidence_has_fields", ["source_ref"])],
        expected_titles=expected_titles,
        forbidden_titles=forbidden_titles or [],
        expected_count_range=expected_count_range,
        memory_target=memory_target,
        evaluation_task=query,
        expected_behavior="Recall grounded project-management memories needed for the PM workflow.",
        ground_truth={
            "expected_titles": expected_titles,
            "forbidden_titles": forbidden_titles or [],
            "expected_count_range": list(expected_count_range) if expected_count_range else None,
            "business_metrics": {metric_name: metric_value},
        },
        scoring_rubric={
            "score_type": "business_value_retrieval",
            "pass_threshold": 1.0,
            "criteria": [
                {"name": "recall_required_project_memory", "weight": 0.45},
                {"name": "exclude_stale_or_wrong_memory", "weight": 0.25},
                {"name": metric_name, "weight": 0.30},
            ],
        },
        difficulty=difficulty,
        source_anchor=f"track_m:{case_id}",
        recall_intent=intent,
        notes=f"Track M metric: {metric_name}={metric_value}",
    )


